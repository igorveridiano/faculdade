    private void btnCadastrarAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarAlunoActionPerformed
         for (Disciplina disciplina : tela.listaDisciplinas) {
            if(disciplina.getNome().equals(txtDisciplinaAluno.getText())){
                Aluno aluno = new Aluno(txtNomeAluno.getText(), Integer.parseInt(txtCpfAluno.getText())
                        , txtSenhaAluno.getText(), txtMatriculaAluno.getText());
                aluno.listaDisciplinas.add(disciplina);
                disciplina.listaAlunos.add(aluno);
                tela.listaAlunos.add(aluno);
            }
        }
         for (Aluno aluno : tela.listaAlunos) {
             diretorio = "C://Users//igorv//Desktop//Exercicio Escola";
             nomeArquivo = "alunos.txt";
             dados = dados + aluno.getNome() + ", " + Integer.toString(aluno.getCpf()) + ", " + aluno.getSenha() + "\n" 
                        + aluno.getMatricula() + "\n";
             for (Disciplina disciplina : aluno.listaDisciplinas) {
                dados = dados + disciplina.getNome() + ", " +disciplina.getId() + "\n";
            }
             for (Turma turma : aluno.listaTurmas) {
                 dados = dados + turma.getCodigo() + ", " + turma.getDisciplina().getNome() + ", " + 
                         turma.getProfessor().getNome() + "\n";
             }
        }
         arquivo.Gravar(diretorio, nomeArquivo, dados);
    }//GEN-LAST:event_btnCadastrarAlunoActionPerformed

