    private void btnCadastrarTurmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarTurmaActionPerformed
        for (Disciplina listaDisciplina : tela.listaDisciplinas) {
            for (Professor listaProfessor : tela.listaProfessores) {
                if (listaDisciplina.getNome().equals(txtDisciplinaTurma.getText())){
                    if (listaProfessor.getNome().equals(txtProfessorTurma.getText())){
                        Turma turma = new Turma(txtCodigoTurma.getText(), listaProfessor, listaDisciplina);
                        listaProfessor.listaTurmas.add(turma);
                    }
                }
            }
        }
        for (Turma turmas : tela.listaTurmas) {
            for (Disciplina disciplinas : tela.listaDisciplinas) {
                diretorio = "C://Users//igorv//Desktop//Exercicio Escola";
                nomeArquivo = "turmas.txt";
                dados = dados + turmas.getCodigo() + ", " + turmas.getProfessor().getNome() + turmas.getDisciplina().getNome()
                        + "\n";
            }
            for (Aluno aluno : turmas.listaAlunos) {
                dados = dados + aluno.getNome() + ", " + aluno.getMatricula() + ", " + aluno.getSenha() + ", " 
                        + aluno.getCpf() + "\n";
            }
            arquivo.Gravar(diretorio, nomeArquivo, dados);
        }
    }//GEN-LAST:event_btnCadastrarTurmaActionPerformed

