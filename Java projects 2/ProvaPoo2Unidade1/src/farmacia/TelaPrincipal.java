    private void btnFinalizarVendaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarVendaClienteActionPerformed
        codigo = listaVendas.size() + 1;
        for (Funcionario funcionario : listaFuncionarios) {
            for (Cliente cliente : listaCliente) {
                if (funcionario.getMatricula() == Integer.parseInt(txtCodigoFuncionarioVendaCliente.getText()) && (cliente.getCodigo() == Integer.parseInt(txtCodigoClienteVendaCliente.getText()))) {
                    Venda venda = new Venda(codigo, "Venda a Cliente", cliente.getNome(), funcionario.getNome(), precoTotalVendaCliente);
                    try {
                        BancoDeDados.SalvarVenda(venda);
                    } catch (SQLException ex) {
                        Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (IOException ex) {
                        Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    listaVendas.add(venda);
                    TelaPagamento.precoTotalVenda = precoTotalVendaCliente;
                }
            }
        }
        telaPagamento.setLblPrcoTotalDinheiro(precoTotalVendaCliente);
        telaPagamento.setVisible(true);
        lblPrecoTotalVendaCliente.setVisible(false);
        txtCodigoFuncionarioVendaCliente.setText("");
        txtCodigoClienteVendaCliente.setText("");
        DefaultTableModel model = (DefaultTableModel) tbVendaCliente.getModel();
        for (int i = 0; i < tbVendaCliente.getRowCount(); i++) {
            model.removeRow(i);
        }
    }//GEN-LAST:event_btnFinalizarVendaClienteActionPerformed

    private void btnAdicionarProdutoVendaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarProdutoVendaClienteActionPerformed
        boolean existe = false;
        for (Produto produto : listaProdutos) {
            if (produto.getCodigo() == Integer.parseInt(txtCodigoProdutoVendaCliente.getText())) {
                existe = true;
                if (produto.getQuantidade() >= Integer.parseInt(txtQuantidadeProdutoVendaCliente.getText())) {
                    DefaultTableModel model = (DefaultTableModel) tbVendaCliente.getModel();
                    float precoTotalProduto = Integer.parseInt(txtQuantidadeProdutoVendaCliente.getText()) * produto.getPreco();
                    Object[] informacoes = {produto.getCodigo(), produto.getPreco(), Integer.parseInt(txtQuantidadeProdutoVendaCliente.getText()), precoTotalProduto};
                    model.addRow(informacoes);
                    precoTotalVendaCliente = precoTotalVendaCliente + precoTotalProduto;
                    try (Formatter fmt = new Formatter()) {
                        fmt.format("%.2f", precoTotalVendaCliente);
                        lblPrecoTotalVendaCliente.setText("TOTAL = R$" + fmt);
                    }
                    produto.setQuantidade(produto.getQuantidade() - Integer.parseInt(txtQuantidadeProdutoVendaCliente.getText()));
                    try {
                        BancoDeDados.SalvarProduto(produto);
                    } catch (SQLException ex) {
                        Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (IOException ex) {
                        Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    txtCodigoProdutoVendaCliente.setText("");
                    txtQuantidadeProdutoVendaCliente.setText("");
                } else {
                    TelaQuantidadeInsuficiente telaInsuficiente = new TelaQuantidadeInsuficiente();
                    telaInsuficiente.setVisible(true);
                    txtCodigoProdutoVendaCliente.setText("");
                    txtQuantidadeProdutoVendaCliente.setText("");
                }
            }
        }
        if (existe == false) {
            TelaProdutoNaoCadastrado tela = new TelaProdutoNaoCadastrado();
            tela.setVisible(true);
            txtCodigoProdutoVendaCliente.setText("");
            txtQuantidadeProdutoVendaCliente.setText("");
        }
    }//GEN-LAST:event_btnAdicionarProdutoVendaClienteActionPerformed

