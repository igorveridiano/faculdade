    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        for (Produto listaProduto : listaProdutos) {
            if (listaProduto.getCodigo() == Integer.parseInt(txtCodigoProdutoAtualizar.getText())) {
                if (!"".equals(txtQuantidadeProdutoAtualizar.getText())) {
                    listaProduto.setQuantidade(listaProduto.getQuantidade() + Integer.parseInt(txtQuantidadeProdutoAtualizar.getText()));
                }
                if (!"".equals(txtPrecoProdutoAtualizar.getText())) {
                    listaProduto.setPreco(Float.parseFloat(txtPrecoProdutoAtualizar.getText()));
                }
                if (!"".equals(txtDescricaoProdutoAtualizar.getText())) {
                    listaProduto.setDescricao(txtDescricaoProdutoAtualizar.getText());
                }
                try {
                    BancoDeDados.SalvarProduto(listaProduto);
                } catch (SQLException ex) {
                    Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                }
                txtCodigoProdutoAtualizar.setText("");
                txtQuantidadeProdutoAtualizar.setText("");
                txtPrecoProdutoAtualizar.setText("");
                txtDescricaoProdutoAtualizar.setText("");
                TelaAtualizacaoRealizada tela = new TelaAtualizacaoRealizada();
                tela.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

