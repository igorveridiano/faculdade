    private void btnCadastrarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarProdutoActionPerformed
        Produto produto = new Produto(Integer.parseInt(txtCodigoProduto.getText()), txtDescricaoProduto.getText(), Float.parseFloat(txtPrecoProduto.getText()), Integer.parseInt(txtQuantidadeProduto.getText()));
        try {
            BancoDeDados.SalvarProduto(produto);
        } catch (SQLException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        listaProdutos.add(produto);
        txtCodigoProduto.setText("");
        txtDescricaoProduto.setText("");
        txtPrecoProduto.setText("");
        txtQuantidadeProduto.setText("");
        TelaCadastroEfetuado telaCadastro = new TelaCadastroEfetuado();
        telaCadastro.setVisible(true);
    }//GEN-LAST:event_btnCadastrarProdutoActionPerformed

