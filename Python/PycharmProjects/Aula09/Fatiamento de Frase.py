#Pegando uma letra da frase a partir de uma posição

frase = 'Curso em Video Python'

print(frase[9])

#Pegando um conjunto de letras da frase a partir de uma posição até outra posição menos um

print(frase[9:13])

print(frase[9:21])

#Pegando um conjunto de letras da frase a partir de uma posição até outra posição menos um, pulando de n em n

print(frase[9:21:2])

print(frase[9:21:3])

#Pegando um conjunto de letras da frase a partir de uma posição até outra posição menos um, começando do inicio

print(frase[:5])

#Pegando um conjunto de letras da frase a partir de uma posição até outra posição menos um, começando de uma parter n e indo até o final

print(frase[15:])

#Pegando um conjunto de letras da frase a partir de uma posição até outra posição menos um, começando de uma parte n, indo até o final e pulando de n em n

print(frase[9::3])