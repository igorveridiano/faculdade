package aula02;

public class UsandoMath {

	public static void main(String[] args) {
	double a , b , x , y, m;
	a = 2;
	b = 4;
	m = (a > b)? 0 : 1;
	System.out.println("m = " + m);
	x = Math.pow(a, b);
	y = Math.sqrt(2);
	System.out.println("x = " + x);
	System.out.println("y = " + y);
	System.out.println("Valor da constante Pi = " + Math.PI);
	}

}
